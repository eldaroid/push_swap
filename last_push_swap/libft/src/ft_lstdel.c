#include "libft.h"

void	ft_lstdel(t_list **alst, void (*del)(void*, size_t))
{
	t_list	*next;

	while (*alst)
	{
		next = (*alst)->next;
		del((*alst)->content, (*alst)->content_size);
		if (next == NULL)
		{
			free(*alst);
			*alst = NULL;
		}
		else
			*alst = next;
	}
}
